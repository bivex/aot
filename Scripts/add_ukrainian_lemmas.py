def main():
    with open(MORPHS_PATH, 'r', encoding='utf-8') as f:
        data = json.load(f)

    existing = {l['l'] for l in data['lemmas']}
    added = 0
    for lemma in new_lemmas:
        key = lemma['l']
        if key not in existing:
            data['lemmas'].append(lemma)
            existing.add(key)
            added += 1

    print(f"Added {added} new lemmas (total: {len(data['lemmas'])})")

    with open(MORPHS_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=1)

    print(f"Written to {MORPHS_PATH}")


if __name__ == "__main__":
    main()
